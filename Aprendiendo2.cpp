//Ciclo While

#include <iostream> //Biblioteca

using namespace std; //Utiliza cout y cin

int main() //Funcion Principal del Programa
{
    int i;  // Variable de Tipo Entero

    i = 0; // Iniciando la variable con valor cero

    while (i <=10 )

    {

        cout << i << endl;
        i++;
    }


    system("PAUSE");
    return 0;


}