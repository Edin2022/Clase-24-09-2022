// Ciclo Repetitivo while o Mientras

#include <iostream>

using namespace std;

int main ()
{
    int numero;
    
    cout << "\nIngrese un numero" <<endl;
    cin >> numero;

    // While significa mientra se cumpla la condicion
 
    while ( numero <= 5)
    {
        cout << "\nIngres un numero" <<endl ;
        cin >> numero;

    }
    


    system ("Pause");
    return 0;
}